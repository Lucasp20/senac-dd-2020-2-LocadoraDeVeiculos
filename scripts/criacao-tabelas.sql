CREATE DATABASE DBFLORICAR;

USE DBFLORICAR;

CREATE TABLE CLIENTE (
	IDCLIENTE INT NOT NULL PRIMARY KEY,
    NOME VARCHAR(100),
    EMAIL VARCHAR(100),
    CPF CHAR(11),
    CNH INT,
	CONTATO VARCHAR(20),
    RUA VARCHAR(45),
    NUMERO INT,
    COMPLEMENTO VARCHAR(45),
    BAIRRO VARCHAR(45),
    CIDADE VARCHAR(45),
    ESTADO CHAR(2),
    CEP CHAR(8)
);

CREATE TABLE VEICULO (
	IDVEICULO INT NOT NULL PRIMARY KEY,
    MARCA VARCHAR(45),
    MODELO VARCHAR(45),
    ANOFABRICACAO INT,
    ANOMODELO INT,
    COMBUSTIVEL ENUM('ETANOL', 'GASOLINA', 'FLEX', 'GNV', 'ELETRICO')
);

CREATE TABLE LOCACAO (
	IDLOCACAO INT NOT NULL PRIMARY KEY,
    IDCLIENTE INT NOT NULL,
    IDVEICULO INT NOT NULL,
    DTLOCACAO DATETIME,
    KMLOCACAO INT,
    DTENTREGA DATETIME,
    KMENTREGA INT,
    FOREIGN KEY(IDCLIENTE)
		REFERENCES CLIENTE (IDCLIENTE),
	FOREIGN KEY(IDVEICULO) 
		REFERENCES VEICULO (IDVEICULO)
);

SELECT * FROM CLIENTE;
SELECT * FROM VEICULO;
SELECT * FROM LOCACAO;